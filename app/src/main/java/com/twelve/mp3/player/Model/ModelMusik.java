package com.twelve.mp3.player.Model;

public class ModelMusik {
    private String id, judul, artis, album, genre, lirik, link;

    public String getId() {
        return id;
    }

    public String getJudul() {
        return judul;
    }

    public String getArtis() {
        return artis;
    }

    public String getAlbum() {
        return album;
    }

    public String getGenre() {
        return genre;
    }

    public String getLirik() {
        return lirik;
    }

    public String getLink() {
        return link;
    }
}
